package com.primex.core

/**
 * Returns the sum of two integers.
 *
 * @param a The first integer to sum.
 * @param b The second integer to sum.
 * @return The sum of `a` and `b`.
 *
 * Usage example:
 *
 * ```
 * val a = 3
 * val b = 4
 * val result = sum(a, b) // result is 7
 * ```
 *
 * @author Zakir Sheikh
 * @since 1.0.0
 */
fun sum(a: Int, b: Int): Int {
    return a + b
}